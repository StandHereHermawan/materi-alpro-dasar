package ariefbelajardikampus.materialprodasar.tugas.pertemuan7;

public class LoopingTask1 {
    public static void main(String[] args) {
        /*
        Looping */
        int n = 0;
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 4; j++) {
                System.out.print("*  ");
            }
            System.out.println();
        }
        System.out.print("\n");
    }
}
